                child: CircularProgressIndicator(
                  valueColor: AlwaysStoppedAnimation<Color>(
                    FastPayCheckoutPalette.primary,
                  ),
                ),
              ),
            ),
          ),
      ],
    );
  }
}

class _HostedCheckoutCard extends StatelessWidget {
  const _HostedCheckoutCard({
    required this.session,
    required this.onOpenCheckout,
    required this.onRefreshStatus,
  });

  final PaymentSession? session;
  final Future<void> Function(String checkoutUrl)? onOpenCheckout;
  final Future<void> Function()? onRefreshStatus;

  @override
  Widget build(BuildContext context) {
    final ThemeData theme = Theme.of(context);
    final String? checkoutUrl = session?.checkoutUrl;

    return Column(
      crossAxisAlignment: CrossAxisAlignment.stretch,
      children: <Widget>[
        _CheckoutDetail(label: 'Status', value: session?.status ?? 'initiated'),
        if (session?.paymentId != null)
          _CheckoutDetail(
            label: 'Payment ID',
            value: formatFastPayId(session!.paymentId!),
          ),
        if (session?.reference != null)
          _CheckoutDetail(label: 'Reference', value: session!.reference!),
        if (checkoutUrl != null) ...<Widget>[
          const SizedBox(height: 4),
          Container(
            padding: const EdgeInsets.all(16),
            decoration: BoxDecoration(
              color: FastPayCheckoutPalette.surfaceMuted,
              borderRadius: BorderRadius.circular(18),
            ),
            child: SelectableText(
              checkoutUrl,
              style: theme.textTheme.bodyMedium?.copyWith(
                color: FastPayCheckoutPalette.primaryDark,
                height: 1.45,
                fontWeight: FontWeight.w600,
              ),
            ),
          ),
        ],
        const SizedBox(height: 20),
        if (checkoutUrl != null && onOpenCheckout != null)
          FilledButton(
            style: FilledButton.styleFrom(
              backgroundColor: FastPayCheckoutPalette.primary,
              foregroundColor: Colors.white,
              minimumSize: const Size.fromHeight(54),
              shape: RoundedRectangleBorder(
                borderRadius: BorderRadius.circular(18),
              ),
              textStyle: theme.textTheme.titleMedium?.copyWith(
                fontWeight: FontWeight.w700,
              ),
            ),
            onPressed: () async => onOpenCheckout!(checkoutUrl),
            child: const Text('Open secure checkout'),
          ),
        if (checkoutUrl != null && onOpenCheckout != null)
          const SizedBox(height: 12),
        OutlinedButton(
          style: OutlinedButton.styleFrom(
            foregroundColor: FastPayCheckoutPalette.textPrimary,
            side: const BorderSide(color: FastPayCheckoutPalette.border),
            minimumSize: const Size.fromHeight(54),
            shape: RoundedRectangleBorder(
              borderRadius: BorderRadius.circular(18),
            ),
            textStyle: theme.textTheme.titleMedium?.copyWith(
              fontWeight: FontWeight.w700,
            ),
          ),
          onPressed: onRefreshStatus == null
              ? null
              : () async => onRefreshStatus!(),
          child: const Text('Check payment status'),
        ),
      ],
    );
  }
}

